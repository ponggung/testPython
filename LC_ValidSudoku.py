def isValidSudoku(board):

    import numpy as np
    board = np.array(board)

    def isRept(nums):
        nums = [x for x in nums if x != "."]
        if len(set(nums)) != len(nums):
            return True

    for i in range(len(board)):
        rows = list(board[i, :])
        cols = list(board[:, i])
        if isRept(rows) or isRept(cols):
            return False

    for i in range(3):
        for j in range(3):
            block = list(board[3 * i:3 * i + 3, 3 * j:3 * j + 3].flatten())
            if isRept(block):
                return False

    return True


